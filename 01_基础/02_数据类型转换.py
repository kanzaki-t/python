# int(x) 将x转换成一个整数
str1 = "10"
num1 = int(str1)
print(type(str1))  # <class 'str'>
print(type(num1))  # <class 'int'>

# float(x) 将x转换成一个浮点数
str2 = "10.88"
num2 = float(str2)
print(type(str2))  # <class 'str'>
print(type(num2))  # <class 'float'>

# str(x) 将x转换成一个字符串
num3 = 99
str1 = str(num3)
print(type(num3))  # <class 'int'>
print(type(str1))  # <class 'str'>


